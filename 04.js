// win 6 points + move pts
// draw 3 points + move pts
// lose 0 points + move pts

// A  = ROCK => 1 pt
// B  = PAPER => 2 pts
// C  = SCISSORS => 3 pts

// X => LOSE
// Y => DRAW
// Z => WIN

let totalPoints = 0;

processedInput.forEach((round) => {
  let microTotal = 0;
  let opponent = round[0];
  let result = round[1];

  microTotal = microTotal + movePointAssigner(opponent, result);

  // winning
  // rock paper win
  // paper scissors win
  // scissors rock win

  // adds in the round result points.
  if (result === "Y") {
    microTotal = microTotal + 3;
  } else if (result === "Z") {
    microTotal = microTotal + 6;
  } else {
    microTotal = microTotal + 0;
  }

  // finalizes total points
  totalPoints = totalPoints + microTotal;
});

console.log(totalPoints);

// converts letters to RPS
function movePointAssigner(opponentMove, roundResult) {
  let assignment;

  switch (opponentMove) {
    case "A": // ROCK
      switch (roundResult) {
        case "X": // LOSE
          assignment = 3; // SCISSORS
          break;

        case "Y": // DRAW
          assignment = 1; // ROCK
          break;

        case "Z": // WIN
          assignment = 2; // PAPER
          break;
      }
      break;
    case "B": // PAPER
      switch (roundResult) {
        case "X": // LOSE
          assignment = 1; // ROCK
          break;

        case "Y": // DRAW
          assignment = 2; // PAPER
          break;

        case "Z": // WIN
          assignment = 3; // SCISSORS
          break;
      }
      break;
    case "C": // SCISSORS
      switch (roundResult) {
        case "X": // LOSE
          assignment = 2; // PAPER
          break;

        case "Y": // DRAW
          assignment = 3; // SCISSORS
          break;

        case "Z": // WIN
          assignment = 1; // ROCK
          break;
      }
      break;
    default:
      break;
  }

  return assignment;
}
